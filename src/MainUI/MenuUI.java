    //Al clickear eliminar te pide el id del usuarios que deseas eliminar de tus enlaces, revisa que sea cierto y lo elimina
    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        // TODO add your handling code here:
        while (true) {
            int id = Funciones.stringToInt(JOptionPane.showInputDialog(null, "Ingresa el id del usuario.")); //Validacion del String para entero
            if (id != 0) {
                if (app.getList().search(id)) { //Busca el valor del id pedido en la lista de usuarios
                    if (app.getList().search(user.getId()) != true) { //Si el usuario no se encuentra en la lista de usuarios es porque esta vacio
                        JOptionPane.showMessageDialog(null, "No puedes eliminar si esta vacio");
                        break;
                    }
                    app.eliminarArco(user.getId(), id); //Pasas los parametros y llamas la funcion eliminar arco
                    break;
                }
            } else {
                JOptionPane.showMessageDialog(null, "Algun valor esta errado");
            }
        }

    }//GEN-LAST:event_jLabel6MouseClicked
