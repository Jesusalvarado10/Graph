    //Al clickear añadir usuarios dispondras de inputs para que puedas completar la funcion
    private void añadirUsuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_añadirUsuMouseClicked
        // TODO add your handling code here:
        while (true) {
            int id = Funciones.stringToInt(JOptionPane.showInputDialog(null, "Ingresa el id del usuario.")); //Codigo del usuario
            int value = Funciones.stringToInt(JOptionPane.showInputDialog(null, "El tiempo de amistad (Valor) ")); //El valor del arco
            if (id != 0 && value != 0 && value < 20) { //Validacion ya que es imposible tener 100 años/meses de amistad con alguien
                if (!app.getList().search(user.getId())) {  //Se agrega a la lista de usuarios, por si el usuario es nuevo y proviene de iniciar sesion
                    app.getList().addLast(user);    //o no tenia ningun arco en su vertice
                }
                app.edgeUser(user.getId(), id, value); //Se pasan los parametros id del usuario, el id del destino y el valor del enlace
                break;
            } else {
                JOptionPane.showMessageDialog(null, "Algun valor esta errado");
            }
        }


    }//GEN-LAST:event_añadirUsuMouseClicked
