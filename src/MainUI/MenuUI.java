    //Metodo que te pregunta que deseas visualizar y dependiendo de tu eleccion, te abre una pestaña con esa informacion
    public void mostrarGraf() {
        JFrame j = new JFrame(); //Creas una nueva pestaña para poder pasar los valores que traeremos
        TextArea a = new TextArea();
        a.setEditable(false);
        j.setSize(400, 300);
        j.setLocationRelativeTo(null);
        j.add(a);
        String option = JOptionPane.showInputDialog("1.Visualmente\n2.DFS\n3.BFS");
        switch (option) {
            case "1":
                Nose n = new Nose(); 
                n.mostrarGrafo(app); //Muestras una pestaña donde puedes ver el grafo 
                break;
            case "2":
                a.setText(app.mostrarGrafoDFS()); //Pasa el recorrido de los grafos por DFS
                j.setVisible(true);
                j.setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
                break;
            case "3":
                a.setText(app.mostrarGrafoBFS());//Pasa el recorrido de los grafos por BFS
                j.setVisible(true);
                j.setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
                break;
            default:
                JOptionPane.showInternalMessageDialog(null, "Error");

        }
    }//FIn del metodo
