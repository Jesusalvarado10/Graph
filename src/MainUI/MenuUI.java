    public void escribirUser(Lista x) {
        String text = "\n\n     Lista de Usuarios\n\n";
        for (int i = 0; i < x.size(); i++) {
            User aux = (User) x.getValuePosition(i);
            if (aux != this.user) {
                text += (aux.getId()) + ".  " + aux.getName() + "\n";

            }
        }
        ListUser.setText(text);
    }//FIn del metodo
